package general_tasks;

public class switchCase {
    public static void main(String[] args) {

        char ad = 'b';


        switch (ad) {
            case 'a':
                System.out.println("your is a");
                break;
            case 'b':
                System.out.println("your is b");
                break;
            default:
                System.out.println("Invalid");
        }
    }
}
